<p style="text-align:justify">	
1.	The Committee will function as per the guidelines issued by the Chairman/Principal.<br><br>
2.	The committee shall meet at least twice a month or whenever it is required or called for by the Chair to discuss.<br><br>
3.	The term of Office of the committee for the academic year 2015-16.<br><br>
