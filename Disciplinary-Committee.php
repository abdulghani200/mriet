<h4><b><u>DUTIES AND RESPONSIBILITIES / FUNCTIONS OF COLLEGE DISCIPLINARY COMMITTEE.</u></b></h4><br>
										
<p style="text-align:justify">	
i.	The college Disciplinary Committee will function under the direct control of the Principal and will have the following functions.<br><br>
ii.	All the members of the committee will function as a team and not as independent entities and they also will not take any decision which may against the interests of either party.<br><br>
iii.	The committee will ensure disciplined behavior by all the students as well as the other staff members.<br><br>
iv.	The member secretary ie Mr. Raghupal Reddy , AO, MRIET  will  take care of the disciplinary aspects of the students belonging to the 1st year B Tech course., and he will  at all times be on the lookout  for any happenings involving these students amongst themselves or with the other students. <br><br>
v.	The heads of the respective Departments will be responsible for the overall discipline of the staff members.<br><br>
vi.	Cases of  / any incidents of indiscipline of what so ever nature will be initially subjected to a preliminary enquiry by the committee members and immediately after that   at the first opportunity be brought to the notice of the Principal and as per his advise will either be referred to the Civil police or parents of the students.<br><br>
vii.	Depending upon the gravity of the act of indiscipline, matter may be investigated further by the committee and suggest the type of Punishment to be awarded to the students.<br><br>
viii.	As far as the staff members are concerned , immediately an incident of indiscipline comes to light , the HOD shall ask for the written explanation from the staff member concerned and with his remarks put up to the Principal.<br><br>
ix.	If the staff member’s explanation is convincing and  prima facie it is proved that  the staff is innocent , then the committee  may close the issue with the chairman’s remarks .On the contrary if   it is felt that  the staff member is trying to disown his fault , then the committee may  subject the issue for further enquiry, findings and recommendations.<br><br> 
x.	Based on the findings and recommendations of the committee, the staff member may be meted out with the suggested punishment after obtaining the Chairman’s approval.<br><br>
xi.	In respect of minor incidents of indiscipline such as non-wearing of Identity cards, coming late to the college, early departure without permission. <br><br>
xii.	Absence from the class room, etc, the rules as applicable will be put in to effect and the staff penalized accordingly.<br><br>
xiii.	The disciplinary committee will act with discretion while enquiring in to the incidents involving girl students and female members of the staff.<br><br>
							
									</p>		
