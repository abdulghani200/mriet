<h4><b><u>Functioning of Anti Ragging Squad:</u></b></h4><br>
										
<p style="text-align:justify">							 i.	Every college/Institution shall continue a smaller body to be known as the Anti-ragging Squad to be nominated by the Head of the Institution with such representation as may be considered necessary for maintaining vigil, oversight and patrolling functions and shall representation of variousmembers of the campus community and shall have no outside representation.<br>
						        ii.	It shall be the duty of the Anti-ragging Squad to be called upon to make surprise raids on hostels, and other places Vulnerable to incidents and having the potential for ragging and shall be empowered to inspect such places.  <br>
					
					 iii.	It shall also be the duty of the Anti-ragging Squad to conduct an on-the-spot enquiry into any incidents of ragging referred to it by the Head of the Institution or any member of the faculty or any member of the staff or any student or any parent or guardian or any employee of a service provider or by any other person, as the case may be, and the enquir report along with recommendations shall be submitted to the Anti-Ragging Committee for action. Provided that the Anti-Ragging Squad shall conduct such enquiry observing a fair and transparent procedure and the Principles of natural justice and after giving adequate opportunity to the student or students accused of ragging and other witnesses to place before it the facts, documents and views concerning the incidents of ragging, and considerations such other relevant information as may be required.     <br>
					  	
														
									</p>							
											

