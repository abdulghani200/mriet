<h4><b><u>FUNCTIONS FOR GRIEVANCE REDRESSAL COMMITTEE FOR WOMEN:</u></b></h4><br>
										
<p style="text-align:justify">	
i.	The committee’s important and main function will be to look in to the grievances put forward by the women employees of the college. Irrespective of the fact as whether such a complaint is against a Male Employee or Women employee.<br><br>
ii.	The committee shall receive all the complaints in writing under the proper and correct signature of the complainant The complaints may range from  use of un-parliamentary language , passing unwanted comments, making indecent statements, passing remarks about one’s character / behavior, making indecent gestures , passing and making jokes about one’s efficiency in public, and trying to befriend a lady / female member with  a mala-fide intentions, being harsh to a female member after knowing about her  weaknesses / drawbacks and trying to take undue advantage of the situation etc and may many more      which could be brought under the Heading HARASSMENT ( Physically and Mentally)<br><br>
iii.	They shall initially go through the complaint.<br><br>
iv.	Call for the complainant and have a clear dialogue about the complaint, it’s source, time of happening of the incidents, details of the witnesses around and further consequences if any.<br><br>
v.	They shall carry out an impartial analysis of the complaint, complainant’s own behavioral pattern in and outside the college, her background, her peer group members, others, her performance in the college her antecedents.<br><br>
vi.	After coming to the conclusion that Prima facie the complaint has some substance and deserves to be redressed, as a next step they shall summon the person against whom the complaint has been made  whether Male or Female.<br><br>
vii.	Inform her /Him about the existence complaint against her /Him<br><br>
viii.	Try to know full details of the case from him /her.<br><br>
ix.	Obtain a written report / explanation<br><br>
x.	Reduce all the deliberations to writing.<br><br>
xi.	Call for any other person / witnesses to gain further insight in to the incident.<br><br>
xii.	Listen to them also about the complainant and the person against whom the complaint has been made.<br><br>
xiii.	Reduce every bit of deliberation to writing.<br><br>
xiv.	And after a patient hearing and after taking in to consideration all the facts arrive at a conclusion as to whether prima facie a case exists and the complaint stands.<br><br>
xv.	Write findings and recommend action to be taken.<br><br>
xvi.	If the issue can be sorted out by counseling both the parties the committee shall be wise enough to do so rather than blowing the issue out of proportion.<br><br>
xvii.	In all these deliberations and proceedings, the members shall maintain a calm and quiet composure and behave in an unbiased and impartial manner.<br><br>
xviii.	It may be ensured that at no stage the particulars of the persons involved are made public which may amount to further humiliation of the parties involved.  <br><br>
					</p>	

